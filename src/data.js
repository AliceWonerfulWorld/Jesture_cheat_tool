function toHiragana(value) {
  return value.replace(/[ァ-ヶ]/g, (character) => {
    return String.fromCharCode(character.charCodeAt(0) - 0x60);
  });
}

export function getItemReading(item) {
  return readingOverrides[item] ?? toHiragana(item).toLowerCase();
}

